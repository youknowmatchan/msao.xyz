    <h1 class="text-xl font-semibold">MSAO History</h1> 
    <p>The Miskatonic Southern Antarctic Observatory (MSAO) was open on September 1, 1992 as part of an extension of the Amundsen–Scott South Pole Station. It began being constructed beside other telescope and observatory around 1989, It since has been equip with the state of the art camera and detector and is used to detech any signal from distant galaxy and the void. </p>
    <h1 class="font-semibold text-xl pt-2">Milestone in MSAO History</h1>
    <ul class="list-disc list-inside pl-4 pb-2">
        <li>1989, Project initialize and construction began</li>
        <li>1992, Construction finish and research began</li>
        <li>1992, First comsic microwave background noise detected</li>
        <li>1995, Data collected from the MSAO was used to model clusters of galaxy at unseen before region</li>
        <li>1997, Upgrade has underwent on the telescope to increase the capacity to detect signal from deep space</li>
        <li>1999, Joint analysis with other array was used to gain greater understanding of primodial gravitational wave and cosmiclogical evolution using the polarization of CMB signal</li>
        <li>2001, More analysis with other CMB array was used to understand early expansion of the universe, getting a deep understanding of the inflation period of the universe</li>
    </ul>
